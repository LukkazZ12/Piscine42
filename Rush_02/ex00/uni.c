#include <unistd.h>

void	uni(char *c, char *str)
{	
	int	j;

	j = 0;
	while (str[j] != *c)
		j++;
	while (str[j] < 'a' || str[j] > 'z')
		j++;
	while (str[j] != '\n')
	{
		write(1, &str[j], 1);
		j++;
	}
}
