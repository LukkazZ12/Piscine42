int	ver_uns_int(char *str, int i)
{
	int				j;
	int				k;
	unsigned int	num;

	num = 0;
	j = 0;
	while (j < i)
	{
		k = str[j] - '0';
		if ((num == 429496729 && k >= 6) || num > 429496729)
			return (1);
		num = num * 10 + k;
		j++;
	}
	return (0);
}
